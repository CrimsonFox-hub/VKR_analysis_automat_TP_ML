import re
from .text_processor import TextProcessor

# Первичное определение категории
class RBClassifier:
    def __init__(self):
        self.processor = TextProcessor()
        self.incident_patterns = [
            'невозможность оплаты',
            'не может произвести оплату',
            'не поступают на электронную почту',
            'письмо никуда не уходит',
            'не проходит автоплатеж',
            'не может зайти в лкк',
            'не может провести оплату',
            'выдает ошибку',
            'ошибка в приложение',
            'сумма к оплате отличается',
            'лкк ошибка',
            'произошла ошибка',
            'не работает лкк',
            'не получается войти',
            'не открывается административная панель',
            'не работает админ.панель',
            'не открывается страница',
            'не открывается',
            'не загружается',
            'не отображается',
            'не скачивается',
            'не работает',
            'ошибка',
            'сбой',
            'проблема',
            'неправильно',
            'некорректно',
            'исчезло',
            'пропало',
            'зависает',
            'тормозит'
        ]
        self.routine_patterns = [
            'рассылка квитанций',
            'отправка квитанций',
            'загрузка данных',
            'выгрузка данных',
            'прошу отправить',
            'прошу разослать',
            'прошу предоставить',
            'прошу обновить',
            'прошу внести',
            'прошу помочь',
            'консультация',
            'прошу проконсультировать',
            'прошу проверить',
            'прошу уточнить',
            'настройка',
            'конфигурация'
        ]

    def classify(self, text: str, category: str) -> tuple:
        text_lower = self.processor.text_cleaner(text).lower()
        category_lower = category.lower() if category else ""

        # Проверка категории
        if any(c in category_lower for c in ['орппп - ошибки', 'орппп - лкк - жалобы', 'орппп - лкк - потенциальная ошибка', 'орппп - ошибка данных']):
            return "incident", 0.98, f"Категория указывает на инцидент"
        if any(c in category_lower for c in ['орппп - рассылки', 'орппп - консультация', 'орппп - обслуживание сервисов']):
            return "routine", 0.95, f"Категория указывает на текучку"

        # Проверка паттернов инцидентов
        for pattern in self.incident_patterns:
            if pattern in text_lower:
                return "incident", 0.95, f"Паттерн инцидента: {pattern}"

        # Проверка паттернов текучки
        for pattern in self.routine_patterns:
            if pattern in text_lower:
                # Если есть признаки проблемы, всё же инцидент
                if any(p in text_lower for p in ['не работает', 'ошибка', 'сбой']):
                    continue
                return "routine", 0.90, f"Паттерн текучки: {pattern}"

        # Анализ слова "прошу"
        if 'прошу' in text_lower:
            if any(word in text_lower for word in ['исправить', 'устранить', 'починить', 'решить проблему']):
                return "incident", 0.85, "Просьба исправить проблему"
            else:
                return "routine", 0.80, "Просьба выполнить работу"

        # По умолчанию — текучка
        return "routine", 0.70, "Не удалось определить"